public class PessoaJuridica {
    
    int cnpj;
    String tipo;

    public PessoaJuridica(int cnpj, String tipo) {
        this.cnpj = cnpj;
        this.tipo = tipo;
    }

    public int getCnpj() {
        return cnpj;
    }

    public void setCnpj(int cnpj) {
        this.cnpj = cnpj;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
    
}
