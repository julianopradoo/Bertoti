package modelos;

/**
 *
 * @author Juliano
 */
public class PessoaFisica {
    
    int rg;
    int cpf;
    String tipo;

    public PessoaFisica(int rg, int cpf, String tipo) {
        this.rg = rg;
        this.cpf = cpf;
        this.tipo = tipo;
    }

    public int getRg() {
        return rg;
    }

    public void setRg(int rg) {
        this.rg = rg;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
    
    
    
}
